class Bitwise
{
    public static void main(String[] args) {
        System.out.println("5>>1: "+(5>>1));
        System.out.println("6&10: "+(6&10));//2
       //0110
//       1010   0010   
        System.out.println("6|10:  "+(6|10));
       //0110
//       1010   1110  
        System.out.println("6^10: "+(6^10));
    }
}